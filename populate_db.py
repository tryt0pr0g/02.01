import sqlite3

# Подключение к базе данных
conn = sqlite3.connect('database.db')
cursor = conn.cursor()

# Очистка таблиц (если нужно заново заполнить)
cursor.execute('DELETE FROM news')
cursor.execute('DELETE FROM events')

# Добавление новостей
news_data = [
    ("Футбол", "Сборная Аргентины выиграла Кубок Америки 2024."),
    ("Баскетбол", "Леброн Джеймс объявил о завершении карьеры."),
    ("Теннис", "Новак Джокович выиграл Уимблдон в 10-й раз."),
    ("Формула 1", "Макс Ферстаппен снова стал чемпионом мира."),
    ("Хоккей", "КХЛ объявила о расширении лиги.")
]

for title, content in news_data:
    cursor.execute('INSERT INTO news (title, content) VALUES (?, ?)', (title, content))

# Добавление открытых мероприятий
open_events = [
    ("Футбольный турнир", "Любительский турнир среди городских команд.", "open"),
    ("Бег на 10 км", "Марафон для всех желающих.", "open"),
    ("Теннисный чемпионат", "Турнир среди любителей.", "open"),
    ("Соревнования по плаванию", "Заплыв на открытой воде.", "open"),
    ("Велогонка", "Соревнования среди велосипедистов.", "open")
]

# Добавление закрытых мероприятий с результатами
closed_events = [
    ("Чемпионат по шахматам", "Городской чемпионат по шахматам.", "closed", "Победитель: Иван Иванов."),
    ("Гонка на 5 км", "Забег по пересеченной местности.", "closed", "Победитель: Петр Петров."),
    ("Соревнования по боксу", "Турнир по боксу среди любителей.", "closed", "Победитель: Алексей Смирнов."),
    ("Турнир по баскетболу", "Соревнования среди студенческих команд.", "closed", "Победитель: Команда 'Спартак'."),
    ("Соревнования по лыжам", "Городской чемпионат по лыжным гонкам.", "closed", "Победитель: Сергей Кузнецов.")
]

for name, description, status in open_events:
    cursor.execute('INSERT INTO events (name, description, status) VALUES (?, ?, ?)', (name, description, status))

for name, description, status, results in closed_events:
    cursor.execute('INSERT INTO events (name, description, status, results) VALUES (?, ?, ?, ?)', (name, description, status, results))

# Сохранение изменений и закрытие соединения
conn.commit()
conn.close()

print("База данных успешно заполнена тестовыми данными!")
